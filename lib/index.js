const useref = () => {
  return src('./temp/*.html', { base: 'temp' })
  .pipe(plugins.useref({ searchPath: ['temp', '.']}))
  .pipe(plugins.if(/\.js$/, plugins.uglify()))
  .pipe(plugins.if(/\.css$/, plugins.cleanCss()))
  .pipe(plugins.if(/\.html$/, plugins.htmlmin({ 
    collapseWhitespace: true,
    minifyCSS: true,
    minifyJS: true,
  })))
  .pipe(dest('dist'))
}

// Âπ∂Ë°åÂ§ÑÁêÜ
